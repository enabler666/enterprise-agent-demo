CREATE TABLE requirements (
    id BIGINT NOT NULL AUTO_INCREMENT,
    requirement_no VARCHAR(32) NOT NULL,
    title VARCHAR(200) NOT NULL,
    description TEXT NOT NULL,
    applicant_id VARCHAR(64) NOT NULL,
    applicant_name VARCHAR(100) NOT NULL,
    department VARCHAR(100) NOT NULL,
    type VARCHAR(100) NOT NULL,
    status VARCHAR(32) NOT NULL,
    current_node VARCHAR(100) NOT NULL,
    expected_completion_date DATE NOT NULL,
    created_at TIMESTAMP(6) NOT NULL,
    updated_at TIMESTAMP(6) NOT NULL,
    PRIMARY KEY (id),
    CONSTRAINT uk_requirements_requirement_no UNIQUE (requirement_no),
    INDEX idx_requirements_title (title),
    INDEX idx_requirements_applicant_id (applicant_id),
    INDEX idx_requirements_department_status (department, status),
    INDEX idx_requirements_created_at (created_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

